  const tierMap = {
    micro: [], // 10K-50K followers
    mid: [], // 50K-500K followers
    macro: ['instagram_bedroom_queen', 'instagram_fitness_goddess'], // 500K-1M followers
    mega: ['instagram_fashion_maven'] // 1M+ followers
  };


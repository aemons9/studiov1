// Helper functions
export function getWardrobeByIntimacy(intimacyLevel: number): WardrobeOption[] {
  return VERA_COMPREHENSIVE_WARDROBE.filter(w =>
    w.suitableForIntimacy && w.suitableForIntimacy.includes(intimacyLevel)
  );
}

export function getWardrobeByCoverage(maxCoverage: number): WardrobeOption[] {
  return VERA_COMPREHENSIVE_WARDROBE.filter(w => w.coverageLevel <= maxCoverage);
}

export function getRandomWardrobe(intimacyLevel: number): WardrobeOption {
  const suitable = getWardrobeByIntimacy(intimacyLevel);
  return suitable[Math.floor(Math.random() * suitable.length)] || VERA_COMPREHENSIVE_WARDROBE[0];
}
